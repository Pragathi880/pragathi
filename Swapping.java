//swapping two numbers using third variable
class Swapping{
	public static void main(String[] args){
	int pragathi=30;
	int nidhi=25;
	int temp=0;
	System.out.println("value of pragathi before swapping");
	System.out.println("value of nidhi before swapping");
	System.out.println("value of temp before swapping");
	
	temp=pragathi;
	pragathi=nidhi;
	nidhi=temp;
	System.out.println("value of pragathi after swapping");
	System.out.println("value of nidhi after swapping");
	
	}
}